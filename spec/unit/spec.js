describe("In algebra", function() {
  describe("the plus sign", function() {
    it("should add 2 and 2 together and get 4", function() {
      expect(2 + 2).toEqual(4)
    })

    it("should add 2 and 2 together and get 4", function() {
      expect(2 + 2).toBeGreaterThan(0)
    })

    it("should add 2 and 2 together and get 4", function() {
      expect(2 + 2).toEqual(5)
    })
  })

  describe("the times sign", function() {
    it("should multiply 2 and 4 together and get 8", function() {
      expect(2 * 4).toEqual(8)
    })
  })
})
